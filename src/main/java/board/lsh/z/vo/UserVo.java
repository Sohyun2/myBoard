package board.lsh.z.vo;

public class UserVo {

	private String CD_COMPANY;
	private String NO_EMP;
	private String NM_KOR;
	private String CD_BIZAREA;
	private String CD_DEPT;
	
	public String getCD_COMPANY() {
		return CD_COMPANY;
	}
	public void setCD_COMPANY(String cD_COMPANY) {
		CD_COMPANY = cD_COMPANY;
	}
	public String getNO_EMP() {
		return NO_EMP;
	}
	public void setNO_EMP(String nO_EMP) {
		NO_EMP = nO_EMP;
	}
	public String getNM_KOR() {
		return NM_KOR;
	}
	public void setNM_KOR(String nM_KOR) {
		NM_KOR = nM_KOR;
	}
	public String getCD_BIZAREA() {
		return CD_BIZAREA;
	}
	public void setCD_BIZAREA(String cD_BIZAREA) {
		CD_BIZAREA = cD_BIZAREA;
	}
	public String getCD_DEPT() {
		return CD_DEPT;
	}
	public void setCD_DEPT(String cD_DEPT) {
		CD_DEPT = cD_DEPT;
	}
	
	
}
